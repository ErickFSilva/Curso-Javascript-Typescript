// Regras de constantes:

/**
 * Não podemos criar constantes com palavras reservadas;
 * Constantes precisam ter nomes significativos;
 * Não pode começar o nome de uma constante com um número;
 * Constantes não podem conter espaços ou traços;
 * Utilizamos camelCase;
 * Case-sensitive;
 * Não podemos modificar o valor de uma constante;
 * Crie constantes, preferencialmente, com 'const';
 */


// Constante do tipo string:
const nome = 'Erick';
console.log(nome);

// Constante do tipo number:
const anoAtual = 2023;
console.log(anoAtual);

// Verifica o tipo do dado recebido:
console.log(`Tipo da constante: ${typeof nome}`);
console.log(`Tipo da constante: ${typeof anoAtual}`);
