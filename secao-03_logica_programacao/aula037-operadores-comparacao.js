/**
 * Operadores de comparação:
 * 
 * >: Maior quê;
 * >=: Maior quê ou igual;
 * <: Menor quê;
 * <=: Menor quê ou igual;
 * ==: Igualdade (valor);
 * ===: Igualdade estrita (valor e tipo);
 * !=: Diferente;
 * !==: Diferente estrito;
 */


// Constantes
const num1 = 10;
const num2 = '11';

// Maior quê:
console.log(num1 > num2);

// Maior quê ou igual:
console.log(num1 >= num2);

// Menor quê:
console.log(num1 < num2);

// Menor quê ou igual:
console.log(num1 <= num2);

// Igualdade (valor):
console.log(num1 == num2);

// Igualdade estrita (valor e tipo):
console.log(num1 === num2);

// Diferente:
console.log(num1 != num2);

// Diferente estrito:
console.log(num1 !== parseInt(num2));