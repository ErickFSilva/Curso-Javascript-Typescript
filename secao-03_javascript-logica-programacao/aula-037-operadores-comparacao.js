/** 
 * OPERADORES DE COMPARAÇÃO
 * 
 * > Maior que
 * >= Maior que ou igual a
 * < Menor que
 * <= Menor que ou igual a
 * == Igualdade, checa o valor
 * === Igualdade estrita, checa o valor e o tipo
 * != diferente, checa o valor
 * !== diferente estrito, checa o valor e o tipo
*/


let valor1 = 100;
let valor2 = 200;
let valor3 = '100';

console.log(valor1 > valor2);
console.log(valor2 >= valor3);
console.log();

console.log(valor3 < valor1);
console.log(valor1 <= valor2);
console.log();

console.log(valor1 == valor3);
console.log(valor3 === valor1);
console.log();

console.log(valor1 != valor2);
console.log(valor2 !== valor3);
console.log();